# Let's see a big number:

print('\n***************************************************\n');

print('Here are some examples of how to show a big')
print('number in python3')

print('\n***************************************************\n');

print("print(100**100)  => \n")
print(100**100)

print('\n***************************************************\n');

print("print(f'{100**100:,}')  => \n")
print(f'{100**100:,}')

print('\n***************************************************\n');

print("num1 = 100**100;\nprint(f'{num1:,}')  => \n")
num1 = 100**100;
print(f'{num1:,}')

